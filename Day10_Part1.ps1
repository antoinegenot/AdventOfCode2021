$nl = [System.Environment]::NewLine
$lines = $inputs -split "$nl"

$totalErrorScore = 0
foreach ($line in $lines) {
    $characters = [char[]]$line
    $openChars = ''
    $errorScore = 0
    
    foreach ($c in $characters) {
        switch ($c) {
            '(' {
                $openChars += '('
                break
            }
            '[' {
                $openChars += '['
                break
            }
            '{' {
                $openChars += '{'
                break
            }
            '<' {
                $openChars += '<'
                break
            }
            ')' {
                if ($openChars[-1] -ne '(') {
                    $errorScore += 3
                }
                else { $openChars = $openChars -replace '\($' }
                break
            }
            ']' {
                if ($openChars[-1] -ne '[') {
                    $errorScore += 57
                }
                else { $openChars = $openChars -replace '\[$' }
                break
            }
            '}' {
                if ($openChars[-1] -ne '{') {
                    $errorScore += 1197
                }
                else { $openChars = $openChars -replace '{$' }
                break
            }
            '>' {
                if ($openChars[-1] -ne '<') {
                    $errorScore += 25137
                }
                else { $openChars = $openChars -replace '<$' }
                break
            }
        }
        if ($errorScore -gt 0) { break }
    }
    $totalErrorScore += $errorScore
}

